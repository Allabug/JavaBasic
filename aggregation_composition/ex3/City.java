package aggregation.ex3;

class City {
    private String nameCity;

    public City(String nameCity) {
        this.nameCity = nameCity;
    }

    public void setNameCity() {
        this.nameCity = nameCity;
    }

    public String getNameCity() {
        return this.nameCity;
    }

    public String toString() {
        return nameCity;
    }
}
